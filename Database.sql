CREATE DATABASE testcases;
USE testcases;

CREATE TABLE Users (
    UserID CHAR(6) PRIMARY KEY,
    UserName NVARCHAR(50) UNIQUE,
    FName NVARCHAR(50),  -- First Name
    LName NVARCHAR(50),  -- Last Name
    Pass NVARCHAR(80),
    Email NVARCHAR(100) UNIQUE,
    Role NVARCHAR(20) CHECK (Role IN ('Admin', 'User', 'Staff')) DEFAULT 'User',
    Phone NVARCHAR(15),
    Sex NVARCHAR(7),
    DateOfBirth DATE CHECK (DateOfBirth <= DATEADD(YEAR, -18, GETDATE())),
    MoneyLeft MONEY CHECK (MoneyLeft >= 0),
    Avatar NVARCHAR(MAX)
);


-- Table for Theatres
CREATE TABLE Theatres(
    TheatreID CHAR(6) PRIMARY KEY,
    TheatreName NVARCHAR(50) UNIQUE,
    TheatreLocation NVARCHAR(150)
);

-- Foods and Drinks Table
CREATE TABLE FoodsAndDrinks (
    ComboID CHAR(6) PRIMARY KEY,
    ComboName NVARCHAR(100),
	TheatreID CHAR(6),
	ImagePath VARCHAR(255),
    Price MONEY CHECK (Price >= 0),
	FOREIGN KEY (TheatreID) REFERENCES Theatres(TheatreID) 
);

-- Table for Booking
CREATE TABLE Booking(
    BookingID CHAR(6) PRIMARY KEY,
    UserID CHAR(6),
	ComboID CHAR(6),
    BookingDate DATE,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);


-- Table for Rooms
CREATE TABLE Rooms(
    RoomID CHAR(6) PRIMARY KEY,
    RoomName NVARCHAR(50),
    TheatreID CHAR(6),
    FOREIGN KEY (TheatreID) REFERENCES Theatres(TheatreID)
);

-- Table for Seats 
CREATE TABLE Seats(
    SeatID CHAR(6) PRIMARY KEY,
    SeatName NVARCHAR(50),
    RoomID CHAR(6),
	TheatreID CHAR(6),
	FOREIGN KEY (RoomID) REFERENCES Rooms(RoomID),
    UNIQUE(RoomID, SeatName)
);

-- Table for Genres
CREATE TABLE Genres (
    GenreID INT PRIMARY KEY IDENTITY(1,1),
    GenreName NVARCHAR(50)
);

-- Table for Movies
CREATE TABLE Movies (
    MovieID CHAR(6) PRIMARY KEY,
    MovieName NVARCHAR(100),
	Duration INT, 
    Country NVARCHAR(50),
	Manufacturer NVARCHAR(100),
    Director NVARCHAR(100),
    ReleaseDate DATE,
	ImgPortrait VARCHAR(512),
	ImgLandscape VARCHAR(512),
	MovieDescription NVARCHAR(4000),
	Rate DECIMAL(2,1) CHECK (Rate >= 0 AND Rate <= 10)
	);


CREATE TABLE Ratings (
    RatingID INT PRIMARY KEY IDENTITY(1,1),
    UserID CHAR(6),
    MovieID CHAR(6),
    Rating DECIMAL(2,1) CHECK (Rating >= 0 AND Rating <= 10),
    CONSTRAINT FK_Movie FOREIGN KEY (MovieID) REFERENCES Movies(MovieID),
    CONSTRAINT FK_User FOREIGN KEY (UserID) REFERENCES Users(UserID)
);


-- Junction table for Movie-Genre relationship
CREATE TABLE MovieGenres (
    MovieID CHAR(6),
    GenreID INT,
    PRIMARY KEY (MovieID, GenreID),
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID),
    FOREIGN KEY (GenreID) REFERENCES Genres(GenreID)
);

-- Table for Shows
CREATE TABLE Shows (
    ShowID CHAR(6) PRIMARY KEY,
	ShowDate DATE, 
    StartTime TIME,  
    MovieID CHAR(6),		
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID)
);

-- Table for linking Seats with Shows (to manage seat availability per show)
CREATE TABLE ShowSeats (
    ShowID CHAR(6),
    SeatID CHAR(6),
	RoomID CHAR(6),
	TheatreID CHAR(6),
    IsAvailable BIT DEFAULT 1,
    PRIMARY KEY (ShowID, SeatID, RoomID, TheatreID),
    FOREIGN KEY (ShowID) REFERENCES Shows(ShowID),
	FOREIGN KEY (SeatID) REFERENCES Seats(SeatID)
);

-- Table for Actors
CREATE TABLE Actors (
    ActorID CHAR(6) PRIMARY KEY,
    ActorName NVARCHAR(100),
	ImagePath VARCHAR(255)
);

-- Table for linking Movies and Actors (many-to-many relationship)
CREATE TABLE MovieActors (
    MovieID CHAR(6),
    ActorID CHAR(6),
    PRIMARY KEY (MovieID, ActorID),
    FOREIGN KEY (MovieID) REFERENCES Movies(MovieID),
    FOREIGN KEY (ActorID) REFERENCES Actors(ActorID)
);

-- Table for Vouchers
CREATE TABLE Vouchers (
    VoucherID CHAR(6) PRIMARY KEY,
	VoucherName NVARCHAR(50) UNIQUE,
	 TheatreID CHAR(6),
    Price MONEY CHECK (Price >= 0),
	ImagePath VARCHAR(255),
    ExpiryDate DATE,
	FOREIGN KEY (TheatreID) REFERENCES Theatres(TheatreID) 
);


-- Table for Tickets (linked to Booking, Shows, and Seats)
CREATE TABLE Tickets (
    TicketID CHAR(6) PRIMARY KEY,
    BookingID CHAR(6),
    ShowID CHAR(6),
	TheatreID CHAR(6),
	RoomID CHAR(6),
    SeatID CHAR(6),
    VoucherID CHAR(6),
    Price MONEY CHECK (Price >= 0),
    BookingDate DATETIME,
	TicketStatus NVARCHAR(10),
    FOREIGN KEY (BookingID) REFERENCES Booking(BookingID),
    FOREIGN KEY (ShowID, SeatID, RoomID, TheatreID) REFERENCES ShowSeats(ShowID, SeatID, RoomID, TheatreID),
    FOREIGN KEY (VoucherID) REFERENCES Vouchers(VoucherID)
);

CREATE table WorkHis(
	WorkId char(6) primary key,
	WorkName char(512),
	WorkDescription char(512),
	StartTime date,
	EndTime date,
	StaffId char(6) 
	foreign key (StaffId) references Users(UserId)
)

CREATE TABLE Report(
	ReportId char(6) primary key,
	ReportTitle char(512),
	ReportDescription char(512),
	TimeCreate date,
	UserId char(6)
	foreign key (UserId) references Users(UserId)
)


